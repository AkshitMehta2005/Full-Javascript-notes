// Detailed Answer

// Storage Capacity

// Cookies → Small (~4KB).

// LocalStorage → Larger (~5–10MB depending on browser).

// Data Expiry

// Cookies → Can have an expiry date, auto-sent with requests.

// LocalStorage → Data persists until manually cleared.

// Accessibility

// Cookies → Accessible by both client and server (sent with every HTTP request).

// LocalStorage → Accessible only on the client side (not sent to server).

// Use Case

// Cookies → Best for server-side sessions, authentication tokens, and small data.

// LocalStorage → Best for storing larger data like user preferences, cached data, or app state.

// Short Interview Version (Quick Recall)

// Cookies: Small (4KB), sent with every request, can expire, used for sessions/authentication.

// LocalStorage: Larger (5–10MB), client-side only, no expiry, used for storing persistent user data.